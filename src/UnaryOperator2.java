class UnaryOperator2 {
    public static void main(String[] args){
        double number = 5.2;

        System.out.println(number++);
        // Gives 5.2

        System.out.println(number);
        // Gives 6.2

        System.out.println(++number);
        // Gives 7.2

        System.out.println(number);
        // Gives 7.2

    }
}
