class ArithmeticOperator {
    public static void main(String[] args){

        double number1 = 12.5, number2 = 3.5, result;

        // Addition
        result = number1 + number2;
        System.out.println("number1 + number2 = " + result);

        // Subtraction
        result = number1 - number2;
        System.out.println("number1 - number2 = " + result);

        // Multiplication
        result = number1 * number2;
        System.out.println("number1 * number2 = " + result);

        // Division
        result = number1 / number2;
        System.out.println("number1 / number2 = " + result);

        // Remainder
        result = number1 % number2;
        System.out.println("number1 % number2 = " + result);

    }
}
